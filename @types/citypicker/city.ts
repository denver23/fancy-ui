function getPath(id: string) {
  const res: any[] = []
  // 递归
  const d = function(n: string) {
    if (!n) {
      return
    }
    for (const v of data) {
      if (v.id === n) {
        res.unshift(v)
        if (Number(v.fid) > 0) {
          d(v.fid)
        }
        return
      }
    }
  }
  id && d(id)
  return res.length > 0 ? res : false
}

